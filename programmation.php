            <div class="col-md-6">
                <h3 class="band-title">Drawers <small>(Sludge/Doom, Toulouse)</small></h3>
                <p class="band-links">
                    <a href="http://drawerskvlt.bandcamp.com/" title="Bandcamp Drawers" target="_blank"><img alt="Bandcamp Drawers" src="img/bandcamp-small.png"></a>
                    <a href="https://www.facebook.com/drawerskvlt" title="Facebook Drawers" target="_blank"><img alt="Facebook Drawers" src="img/facebook-small.png"></a>
                </p>
                <p>Quintet de stoner/doom/hardcore toulousain, Drawers était initialement un projet d’à côté, qui a réellement pris forme à la sortie du premier EP "This Is Oil". Un nouveau chanteur plus tard, Drawers revient en 2011 avec "All Is One", premier long format qui marquera le point de départ d’une tournée européenne. S’ensuit un split avec Hangman’s Chair en 2012 et surtout un nouvel album éponyme paru en février 2014 chez Kaotoxin, puissant brûlot qui rappellera autant les premiers Baroness que Down, Mastodon ou Crowbar (avec qui ils ont fait une date). Grosse claque scénique en prévision donc.</p>
            </div>
        </div>
        <div class="row">
            <div class="col-md-6">
                <img src="img/bands/thou.jpg" alt="Thou Metalorgie Fest 2015" class=" img-responsive band-img">
            </div>
            <div class="col-md-6">
                <h3 class="band-title">Thou <small>(Sludge/Doom, USA)</small></h3>
                <p class="band-links">
                    <a href="http://thou.bandcamp.com/" title="Bandcamp Thou" target="_blank"><img alt="Bandcamp Thou" src="img/bandcamp-small.png"></a>
                    <a href="https://www.facebook.com/pages/Thou/249015501795190" title="Facebook Thou" target="_blank"><img alt="Facebook Thou" src="img/facebook-small.png"></a>
                </p>
                <p>En état de marche depuis 2005, les américains de Baton Rouge nous souillent délicieusement les oreilles via un sludge/doom blackisé aussi massif que subtil. Arborant une imagerie très forte et reconnaissable au travers de ses huit albums, le quintet n’hésite pas à plonger dans les marécages les plus infestés pour en ressortir une substantifique moelle sludge, plombante et garnie de grumeaux de chair en décomposition. Un programme plus que réjouissant qu’on retrouve sur leur dernière offrande, "Heathen", signé sur le même fameux label que Lazer/Wulf, Robotic Empire. Autant dire que ces barons du sludge vont allégrement nous écraser, nous malaxer et nous achever avec grâce et volupté, non sans une pointe de mélancolie pour parfaire le tout, ou le Thou (haha, pardon).</p>
