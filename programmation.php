            <div class="col-md-6">
                <h3 class="band-title">Lazer/Wulf <small>(Metal Expérimental/Fusion/Sludge, USA)</small></h3>
                <p class="band-links">
                    <a href="http://retrofuturist.bandcamp.com/album/the-beast-of-left-and-right" title="Bandcamp Lazer/Wulf" target="_blank"><img alt="Bandcamp Lazer/Wulf" src="img/bandcamp-small.png"></a>
                    <a href="https://www.facebook.com/lazerwulves" title="Facebook Lazer/Wulf" target="_blank"><img alt="Facebook Lazer/Wulf" src="img/facebook-small.png"></a>
                </p>
                <p>Ces américains d'Athens sont trois et pratiquent un amalgame de metal, de jazz, de sludge et de hardcore tout à fait grisant. Un metal "expérimental" nourri d’une multitude de sonorités (citons en vrac The Dillinger Escape Plan, Mastodon, Yakuza, Meshuggah et Tool) qui a explosé dernièrement avec son deuxième album "The Beast of the Left and Right", celui-ci a d’ailleurs naturellement été album du moment dans nos pages. A l’origine ce trio était un quintet avec chanteur, puis il est devenu duo instrumental suite à des soucis de cordes vocales. Les deux restants sont allés chercher le batteur idéal, tentaculaire, fulgurant et gorgé de feeling, depuis c’est l’éclate totale et le guitariste s’est même mis à pousser la chansonnette sur quelques titres, de fort belle manière. Leurs prestations scéniques furieuses (vidéos à l’appui) et leur rareté sur notre sol font qu’aucune putain d’excuse ne sera tolérée, sauf en cas de force majeure évidemment.</p>
            </div>
