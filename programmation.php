                <h2>Vendredi 03 avril</h2>
            </div>
        </div>
        <div class="row">
            <div class="col-md-6">
                <img src="img/bands/aeris.jpg" alt="Aeris Metalorgie Fest 2015" class=" img-responsive band-img">
            </div>
            <div class="col-md-6">
                <h3 class="band-title">Aeris <small>(Metal Progressif/Jazz, Nantes)</small></h3>
                <p class="band-links">
                    <a href="http://aeris.bandcamp.com/" title="Bandcamp Aeris" target="_blank"><img alt="Bandcamp Aeris" src="img/bandcamp-small.png"></a>
                    <a href="https://www.facebook.com/AERISTEMPLE" title="Facebook Aeris" target="_blank"><img alt="Facebook Aeris" src="img/facebook-small.png"></a>
                </p>
                <p>Formé en 2004, Aeris pratique un metal mutant, nourri au jazz, au rock progressif et à la musique improvisé. Un premier album éponyme concrétise la chose en 2009, suivi de trois ans de tournée avec les locaux de Bruxisme et Time For Energy, mais aussi les quimpérois de Corbeaux. En 2013, les nantais d’Aeris renaissent avec un nouveau bassiste et un second guitariste, en découlera un deuxième album, "Temple", sorti chez Ex-Tension Records, complexifiant le propos tout en accentuant et variant les ambiances. Leur prestation devrait ravir les amateurs de structures folles, de toucher guitaristique subtil et de circonvolutions grasses.</p>
            </div>
        </div>
        <div class="row">
            <div class="col-md-6">
                <img src="img/bands/6h33.jpg" alt="6:33 Metalorgie Fest 2015" class=" img-responsive band-img">
            </div>
            <div class="col-md-6">
                <h3 class="band-title">6:33 <small>(Metal Expérimental Déjanté, Paris)</small></h3>
                <p class="band-links">
                    <a href="http://www.633theband.com/" title="Bandcamp 6:33" target="_blank"><img alt="Bandcamp 6:33" src="img/bandcamp-small.png"></a>
                    <a href="https://www.facebook.com/6h33official" title="Facebook 6:33" target="_blank"><img alt="Facebook 6:33" src="img/facebook-small.png"></a>
                </p>
                <p>Les parisiens de 6h33 voient le jour en 2010 et ne perdent pas de temps pour sortir un premier album un an plus tard. L’objet, montrant des influences évidentes du coté de Faith No More et Devin Townsend, sera salué par la critique. S’ensuit une rencontre avec Arno Strobl (Carnival In Coal) en 2012, d’où naîtra un EP, lui aussi acclamé. Le groupe enchaîne avec Strobl en 2013 pour sortir son deuxième album chez Season of Mist. Enfin, le metal fou de 6h33 se voit peaufiné sur un troisième format long, "Deadly Scenes", sorti en janvier 2015, et qui devrait être défendu avec classe et démence en ouverture de ce vendredi.</p>
            </div>
        </div>
        <div class="row">
            <div class="col-md-6">
                <img src="img/bands/kayo-dot.jpg" alt="Kayo Dot Metalorgie Fest 2015" class=" img-responsive band-img">
            </div>
            <div class="col-md-6">
                <h3 class="band-title">Kayo Dot <small>(Psyché/Ambiant/Hardcore/Rock/Inclassable, USA)</small></h3>
                <p class="band-links">
                    <a href="https://kayodot.bandcamp.com/" title="Bandcamp Kayo Dot" target="_blank"><img alt="Bandcamp Kayo Dot" src="img/bandcamp-small.png"></a>
                    <a href="https://www.facebook.com/kayodot.official" title="Facebook Kayo Dot" target="_blank"><img alt="Facebook Kayo Dot" src="img/facebook-small.png"></a>
                </p>
                <p>En termes de bizarrerie, Kayo Dot en est l’un des porte-étendards, écumant les scènes et les disquaires indépendants depuis 2003. La musique des new-yorkais, comptant dans ses rangs d'anciens membres de Maudlin Of The Well dont Toby Driver (également présent chez Secret Chief 3),  est expérimentale, en mutation permanente, chaque disque explorant de nouvelles sonorités, allant du doom progressif à l’abstract, en passant par l’avant-goth, matinées de metal souvent dissonant. Le quintet ne manque pas de créativité puisqu’il a sorti rien de moins que huit albums, traduisant chacun une sensibilité différente, et régulièrement envoûtante. C’est le cas notamment sur le dernier bébé "Coffins On Io", mêlant autant la darkwave ou le black metal que le doom/rock progressif et l’abstract, entre autres. De quoi faire de beaux rêves étranges après cette première soirée.</p>
            </div>
