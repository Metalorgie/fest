            <div class="col-md-6">
                <h2>Samedi 04 avril</h2>
            </div>
        </div>
        <div class="row">
            <div class="col-md-6">
                <img src="img/bands/öfö-am.jpg" alt="ÖfÖ AM Metalorgie Fest 2015" class=" img-responsive band-img">
            </div>
            <div class="col-md-6">
                <h3 class="band-title">ÖfÖ AM <small>(Stoner Psychédélique, Montpellier)</small></h3>
                <p class="band-links">
                    <a href="http://weareofoam.bandcamp.com/" title="Bandcamp ÖfÖ AM" target="_blank"><img alt="Bandcamp ÖfÖ AM" src="img/bandcamp-small.png"></a>
                    <a href="https://www.facebook.com/pages/%C3%96f%C3%96-AM/80706294909" title="Facebook ÖfÖ AM" target="_blank"><img alt="Facebook ÖfÖ AM" src="img/facebook-small.png"></a>
                </p>
                <p>Ce trio montpelliérain évolue dans des sphères stoner-rock noisy proches de Kyuss, et surtout de Karma To Burn, avec qui ils ont eu l’honneur de partager un split album en 2010. Déjà forts d’un très fameux premier long sorti en 2011, ils tourneront ensuite avec Karma To Burn, Year Long Disaster ou encore Huata, et fera même un passage remarqué au Metal Corner du Hellfest 2011. Le groove rampant et les pédales DIY confectionnées par le batteur à moustache rendent le son d’ÖfÖ Am unique et augmentent sans conteste leur aura psychédélique. Un split avec les copains de Lahius suivra en 2014, et un nouvel album devrait sortir en ce début d’année. Le trio ne manquera sans doute pas de nous en extraire quelques titres et de nous les introduire dans nos fébriles esgourdes en ouverture d’un samedi grassouillet.</p>
            </div>
        </div>
        <div class="row">
            <div class="col-md-6">
                <img src="img/bands/lazer-wulf.jpg" alt="Lazer/Wulf Metalorgie Fest 2015" class=" img-responsive band-img">
